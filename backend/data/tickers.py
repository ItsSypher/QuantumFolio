def search_tickers(query: str, limit: int = 10) -> list[dict]:
    """Fuzzy case-insensitive search over ticker DB."""
    q = query.strip().upper()
    if not q:
        return []

    results: list[tuple[int, str, str]] = []

    for ticker, name in TICKER_DB:
        score = 0
        q_lower = q.lower()
        t_lower = ticker.lower()
        n_lower = name.lower()

        # Exact ticker match
        if t_lower == q_lower:
            score = 100
        # Ticker starts with query
        elif t_lower.startswith(q_lower):
            score = 80
        # Company name starts with query word
        elif n_lower.startswith(q_lower):
            score = 70
        # Query contained in ticker
        elif q_lower in t_lower:
            score = 60
        # Query contained in name
        elif q_lower in n_lower:
            score = 40

        if score > 0:
            results.append((score, ticker, name))

    results.sort(key=lambda x: -x[0])
    return [{"ticker": t, "name": n} for _, t, n in results[:limit]]
